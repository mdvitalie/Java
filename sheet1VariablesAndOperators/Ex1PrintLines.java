package sheet1VariablesAndOperators;
public class Ex1PrintLines {

	public static void main(String [] args) {
		
		System.out.println("Today is Wednesday");
		System.out.print("It is 9 degrees\n\n");
		System.out.println("It is going to rain");
		System.out.println("Here is a tab\tand another\t.");
		
		String three = "String three";
		System.out.println("String one" + "String two" + three);
	}
}
